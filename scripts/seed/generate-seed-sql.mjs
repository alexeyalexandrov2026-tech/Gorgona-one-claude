// Generates idempotent seed SQL for public.listings from the canonical seed
// data modules (lib/yachtsData.js, lib/rentalsData.js,
// lib/vacationRentalsData.js, lib/restaurantsNightlifeData.js).
//
// Those files use ESM syntax but live in a CJS package, so we load them by
// concatenating their (import-free, self-contained) source with `export`
// keywords stripped, then evaluating the result as one ESM module. Output:
// scripts/seed/listings.seed.sql - run it against the project database
// (psql, Supabase SQL editor, or the Supabase MCP).
import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath, pathToFileURL } from 'node:url';
import path from 'node:path';
import os from 'node:os';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '../..');
const files = ['lib/yachtsData.js', 'lib/rentalsData.js', 'lib/vacationRentalsData.js', 'lib/restaurantsNightlifeData.js'];

let src = '';
for (const f of files) {
  src += (await readFile(path.join(root, f), 'utf8')).replace(/^export /gm, '') + '\n';
}
src += '\nexport const worlds = { yachts: getYachts(), cars: getRentals(), villas: getVacationRentals(), venues: getVenues() };\n';

const tmp = path.join(os.tmpdir(), `gorgona-seed-${Date.now()}.mjs`);
await writeFile(tmp, src);
const { worlds } = await import(pathToFileURL(tmp).href);

const q = (v) => (v === undefined || v === null ? 'null' : `'${String(v).replace(/'/g, "''")}'`);
const rows = [];
const push = (world, x, i, map) =>
  rows.push(
    `(${q(world)}, ${q(x.slug)}, ${q(map.title)}, ${q(map.subtitle)}, ${q(map.category)}, ${q(x.location)}, ${q(x.description)}, ${q(map.price)}, ${q(x.image)}, ${q(JSON.stringify(map.attrs))}::jsonb, ${x.featured ? 'true' : 'false'}, ${i})`
  );

worlds.yachts.forEach((x, i) =>
  push('yachts', x, i, { title: x.title, subtitle: x.company, category: null, price: x.price, attrs: { legacy_id: x.id, capacity: x.capacity, length: x.length } })
);
worlds.cars.forEach((x, i) =>
  push('car-rentals', x, i, {
    title: x.title, subtitle: x.company, category: x.category, price: x.dailyPrice,
    attrs: { legacy_id: x.id, weeklyPrice: x.weeklyPrice, monthlyPrice: x.monthlyPrice, securityDeposit: x.securityDeposit, availability: x.availability }
  })
);
worlds.villas.forEach((x, i) =>
  push('vacation-rentals', x, i, { title: x.title, subtitle: null, category: null, price: x.nightlyRate, attrs: { legacy_id: x.id, guests: x.guests, bedrooms: x.bedrooms } })
);
worlds.venues.forEach((x, i) =>
  push('dining-nightlife', x, i, { title: x.name, subtitle: null, category: x.category, price: null, attrs: { legacy_id: x.id, rating: x.rating } })
);

const sql = `-- Generated by scripts/seed/generate-seed-sql.mjs - do not edit by hand.
insert into public.listings
  (world, slug, title, subtitle, category, location, description, price_text, image_url, attrs, featured, sort_order)
values
${rows.join(',\n')}
on conflict (world, slug) do update set
  title = excluded.title, subtitle = excluded.subtitle, category = excluded.category,
  location = excluded.location, description = excluded.description, price_text = excluded.price_text,
  image_url = excluded.image_url, attrs = excluded.attrs, featured = excluded.featured,
  sort_order = excluded.sort_order, updated_at = now();
`;
await writeFile(path.join(root, 'scripts/seed/listings.seed.sql'), sql);
console.log(`Wrote ${rows.length} rows to scripts/seed/listings.seed.sql`);
